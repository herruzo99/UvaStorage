    /*Actuliza el bot칩n de encendido para pulsarlo cuando est칠 encendido.*/
    public void actualizaEncendido() {
        if (modelo.getcalEncendida()) {
            jToggleButtonEncender.setSelected(true);
        } else {
            jToggleButtonEncender.setSelected(false);
        }

    }

 
    /*Actualiza el Slider para ponerlo en la posici칩n que corresponda.*/
    public void actualizaCalor() {
        jSlider1.setValue(modelo.temperatura);
    }

    /*
        Para calcular el valor se quitan los primeros 8 px del slider y los 8 칰ltimos,
        despu칠s comprobamos donde se ha pulsado en el slider y se proporciona
        al tama침o del slider.
     */
    public int posicionSlider(int x) {
        return (int) ((jSlider1.getMinimum()
                + ((x - TAM_EXTRA_SLIDER) / ((jSlider1.getWidth() - TAM_EXTRA_SLIDER * 2) * 1.0))
                * (jSlider1.getMaximum() - jSlider1.getMinimum())) + 0.5);
    }
